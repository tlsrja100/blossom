package com.blossom.dto;

public class ReserveDto {

	private int rno;
	private String day;
	private String time;
	private int dno_seq;
	private int cno_seq;
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getDno_seq() {
		return dno_seq;
	}
	public void setDno_seq(int dno_seq) {
		this.dno_seq = dno_seq;
	}
	public int getCno_seq() {
		return cno_seq;
	}
	public void setCno_seq(int cno_seq) {
		this.cno_seq = cno_seq;
	}
	

	
}
